def print_title (title, size=100, character="#"):

    # Print centered title
    print("", flush=True)
    print(character * size, flush=True)
    print(character + " " * ((size - len(title) - 1) // 2) + title + " " * ((size - len(title) - 2) // 2) + character, flush=True)
    print(character * size, flush=True)
    print("", flush=True)

